    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $name = $request->get('name');
        $newCategory = new \App\Models\Category;
        $newCategory->name = $name;
        if ($request->file('image')) {
            $image_path = $request->file('image')->store('category_images', 'public');
            $newCategory->image = $image_path;
            // dd($newCategory);
        }
        $newCategory->created_by = \Auth::user()->id;
        $newCategory->slug = Str::slug($name, '-');
        $newCategory->save();
        return redirect()->route('categories.create')->with('status', 'Sukses terbuat');
    }

